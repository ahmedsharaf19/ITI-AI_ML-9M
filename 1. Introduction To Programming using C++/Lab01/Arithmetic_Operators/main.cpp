#include <iostream>

using namespace std;

int main()
{
    float numberOne, numberTwo;
    cout << "Please Enter TwoNumbers : ";
    cin >> numberOne >> numberTwo;

    cout << numberOne << " + " << numberTwo << " = " << numberOne + numberTwo << endl;

    cout << numberOne << " - " << numberTwo << " = " << numberOne - numberTwo << endl;

    cout << numberOne << " * " << numberTwo << " = " << numberOne * numberTwo << endl;

    cout << numberOne << " / " << numberTwo << " = " << numberOne / numberTwo << endl;

    return 0;
}
